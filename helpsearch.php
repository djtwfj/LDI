<div style="font-size:14px;">
	<ul>
		<li>Any word or part of the company's name.</li>
		<li>A member company contact's last name or first name.</li>
		<li>A member company's type of service or product, like "builder" or "electrical" or "concrete". </li>
		<li>Entering "industrial" in the search field will provide a list of members who have "industrial" in  <br />
